// ν_p(n!)
ll nuOfFact(ll p, ll n) {
  ll nu = 0;
  ll x = p;
  while (x <= n) {
    nu += n / x;
    x *= p;
  }
  return nu;
}

void mainCase() {
  ll n;
  cin >> n;
  // DD(n);

  // max{e | 10^e | n! } = min(ν_2(n!), v_5(n!))
  ll nu2 = nuOfFact(2, n);
  ll nu5 = nuOfFact(5, n);
  ll res = min(nu2, nu5);
  // DD(tie(nu2, nu5, res));

  cout << res << endl;
}

