void mainCase() {
  int n; // [1, 10^5]
  cin >> n;
  vector<array<ll, 2>> ls(n); // [1, 10^6]
  cin >> ls;

  auto tri = [](ll x) -> ll { return (x * (x + 1)) / 2; };

  ll res = 0;
  for (auto [x, y] : ls) {
    res += tri(y) - tri(x - 1);
  }
  cout << res << "\n";
}

