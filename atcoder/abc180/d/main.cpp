// Main
void mainCase() {
  ll x0, x1, a, b; // x \in [1, 10^18], a \in [2, 10^9], b \in [1, 10^9]
  cin >> x0 >> x1 >> a >> b;

  // (x, y) -> (ax,    y + 1)
  //        -> (x + b, y + 1)

  // Thus, choice depends on (a - 1) x  vs  b

  ll x = x0;
  ll k1 = 0;
  // while ((a * x) < x1 && (a - 1) * x < b) { // Overflow by multiplication
  while (a <= (x1 - 1) / x && (a - 1) <= b / x) {
    k1++;
    x = a * x;
  }
  ll k2 = (x1 - 1 - x) / b;

  ll res = k1 + k2;
  cout << res << "\n";
}

int main() {
  ios_base::sync_with_stdio(0); cin.tie(0);
  mainCase();
  return 0;
}

