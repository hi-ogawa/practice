fn main() {
    // [1, 10^18]
    let n: usize = read_vec()[0];
    let k = n.to_string().len();

    // Vn = n * (1 + 10^k + ... + 10^(k (n - 1)))
    //    = n * (10^(k n) - 1) / (10^k - 1)
    let result =
        Mint(n % MODULO) * (Mint(10).pow(k).pow(n) - Mint(1)) / (Mint(10).pow(k) - Mint(1));
    println!("{}", result.0);
}

