void mainCase() {
  ll n, m, r, c; // [1, 10^9]
  cin >> n >> m >> r >> c;

  array<array<ll, 2>, 4> ls = {{
    {1, 1},
    {1, m},
    {n, 1},
    {n, m}
  }};
  ll res = 0;
  for (auto [x, y] : ls) {
    ll t = abs(x - r) + abs(y - c);
    res = max(res, t);
  }
  cout << res << "\n";
}

