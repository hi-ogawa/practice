// Main
vector<tuple<ll, ll>> factorize(ll x) {
  assert(x > 0);
  vector<tuple<ll, ll>> res;
  for (ll p = 2; p * p <= x; p++) {
    ll e = 0;
    while (x % p == 0) { e++; x /= p; }
    if (e > 0) { res.push_back({p, e}); }
  }
  if (x > 1) { res.push_back({x, 1}); }
  return res;
}

void mainCase() {
  ll x; // <= 10^6
  cin >> x;

  // (0th) divisor function
  // Ïƒ(p^e) = e + 1 with multiplicativity
  auto fs = factorize(x);
  ll res = 1;
  for (auto [_p, e] : fs) {
    res *= (e + 1);
  }
  cout << res << endl;
}

